    log_info "–°–æ–∑–¥–∞–µ–º –Ω–æ–≤–æ–µ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ..."
    python3 -m venv venv
    source venv/bin/activate
fi

# –û–±–Ω–æ–≤–ª—è–µ–º pip
