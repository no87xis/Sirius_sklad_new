log_info "–í–µ—Ç–∫–∞: $BRANCH"

# –ü–µ—Ä–µ—Ö–æ–¥–∏–º –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞
cd "$PROJECT_PATH"

# =============================================================================
# –≠–¢–ê–ü 1: –û–°–¢–ê–ù–û–í–ö–ê –í–°–ï–• –°–ï–†–í–ò–°–û–í
# =============================================================================
log_info "–≠–¢–ê–ü 1: –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –≤—Å–µ —Å–µ—Ä–≤–∏—Å—ã..."

