#!/bin/bash

# =============================================================================
# –ö–û–ú–ü–õ–ï–ö–°–ù–û–ï –û–ë–ù–û–í–õ–ï–ù–ò–ï –°–ï–†–í–ï–†–ê SIRIUS GROUP
# =============================================================================
# –≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –ø–æ–ª–Ω–æ—Å—Ç—å—é –æ–±–Ω–æ–≤–ª—è–µ—Ç —Å–µ—Ä–≤–µ—Ä —Å –Ω—É–ª—è
# –í–∫–ª—é—á–∞–µ—Ç: –æ—Å—Ç–∞–Ω–æ–≤–∫—É —Å–µ—Ä–≤–∏—Å–æ–≤, –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–¥–∞, –º–∏–≥—Ä–∞—Ü–∏–∏ –ë–î, –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫
# =============================================================================

