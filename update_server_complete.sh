BACKUP_FILE="backup_$(date +%Y%m%d_%H%M%S).db"

if [ -f "$DB_PATH" ]; then
    mkdir -p "$BACKUP_DIR"
    cp "$DB_PATH" "$BACKUP_DIR/$BACKUP_FILE"
    log_success "–†–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è —Å–æ–∑–¥–∞–Ω–∞: $BACKUP_DIR/$BACKUP_FILE"
else
    log_warning "–ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω–∞, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º —Ä–µ–∑–µ—Ä–≤–Ω–æ–µ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ"
fi

# =============================================================================
# –≠–¢–ê–ü 3: –û–ë–ù–û–í–õ–ï–ù–ò–ï –ö–û–î–ê –ò–ó GIT
# =============================================================================
log_info "–≠–¢–ê–ü 3: –û–±–Ω–æ–≤–ª—è–µ–º –∫–æ–¥ –∏–∑ Git..."

