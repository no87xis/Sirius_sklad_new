if systemctl is-active --quiet sirius-app.service 2>/dev/null; then
    log_info "–û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º sirius-app.service..."
    sudo systemctl stop sirius-app.service
    sudo systemctl disable sirius-app.service
fi

# –£–±–∏–≤–∞–µ–º –≤—Å–µ –ø—Ä–æ—Ü–µ—Å—Å—ã Python –Ω–∞ –ø–æ—Ä—Ç—É 8000
log_info "–ü—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–æ—Ü–µ—Å—Å—ã –Ω–∞ –ø–æ—Ä—Ç—É 8000..."
if lsof -ti:8000 > /dev/null 2>&1; then
    log_info "–ù–∞–π–¥–µ–Ω—ã –ø—Ä–æ—Ü–µ—Å—Å—ã –Ω–∞ –ø–æ—Ä—Ç—É 8000, –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º..."
    lsof -ti:8000 | xargs -r kill -9
