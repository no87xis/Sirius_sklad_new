    "app/main.py"
    "app/database.py"
    "requirements.txt"
    ".env"
)

for file in "${REQUIRED_FILES[@]}"; do
    if [ -f "$file" ]; then
        log_success "‚úì $file –Ω–∞–π–¥–µ–Ω"
    else
        log_warning "‚ö† $file –Ω–µ –Ω–∞–π–¥–µ–Ω"
    fi
