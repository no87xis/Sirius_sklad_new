    log_warning "–ü–æ—Ä—Ç 8000 –≤—Å–µ –µ—â–µ –∑–∞–Ω—è—Ç, –ø—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ –æ—Å–≤–æ–±–æ–∂–¥–∞–µ–º..."
    lsof -ti:8000 | xargs -r kill -9
    sleep 2
fi

log_success "–í—Å–µ —Å–µ—Ä–≤–∏—Å—ã –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã"

# =============================================================================
# –≠–¢–ê–ü 2: –†–ï–ó–ï–†–í–ù–ê–Ø –ö–û–ü–ò–Ø –ë–ê–ó–´ –î–ê–ù–ù–´–•
# =============================================================================
log_info "–≠–¢–ê–ü 2: –°–æ–∑–¥–∞–µ–º —Ä–µ–∑–µ—Ä–≤–Ω—É—é –∫–æ–ø–∏—é –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö..."

