    echo -e "${RED}[ERROR]${NC} $1"
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤
if [ $# -eq 0 ]; then
    log_error "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: $0 <–ø—É—Ç—å_–∫_–ø—Ä–æ–µ–∫—Ç—É> [branch_name]"
    log_error "–ü—Ä–∏–º–µ—Ä: $0 /home/user/sirius main"
    exit 1
