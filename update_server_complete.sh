# =============================================================================
# –ó–ê–í–ï–†–®–ï–ù–ò–ï
# =============================================================================
log_success "=================================================================="
log_success "–ö–û–ú–ü–õ–ï–ö–°–ù–û–ï –û–ë–ù–û–í–õ–ï–ù–ò–ï –°–ï–†–í–ï–†–ê –ó–ê–í–ï–†–®–ï–ù–û –£–°–ü–ï–®–ù–û!"
log_success "=================================================================="
log_success "–°–µ—Ä–≤–µ—Ä –¥–æ—Å—Ç—É–ø–µ–Ω –ø–æ –∞–¥—Ä–µ—Å—É: http://localhost:8000/"
