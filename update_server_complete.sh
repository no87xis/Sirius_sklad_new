pkill -f "python.*main.py" || true
pkill -f "uvicorn.*main:app" || true

# –ñ–¥–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –ø—Ä–æ—Ü–µ—Å—Å–æ–≤
sleep 3

